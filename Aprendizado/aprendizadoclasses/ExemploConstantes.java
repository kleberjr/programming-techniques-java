package aprendizadoclasses;

public class ExemploConstantes {
	/* O modifcador de acesso final não permite que o valor do atributo
	 * seja alterado posteriormente. */
	public final int NUMERO1 = 6;
	
	/* O modificador de acesso static determina o atributo como pertencente
	 * exclusivamente a classe, e não as suas instâncias. Isto é, todas as 
	 * instâncias terão o mesmo valor para o atributo, uma vez que for declarado. */
	public static int numero2;
	
}
